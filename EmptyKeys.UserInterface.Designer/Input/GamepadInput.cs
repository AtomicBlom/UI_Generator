
namespace EmptyKeys.UserInterface.Designer.Input
{
    /// <summary>
    /// Enumeration of all types of game pad input
    /// </summary>
    public enum GamepadInput
    {
        /// <summary>
        /// The none
        /// </summary>
        None,

        /// <summary>
        /// A button
        /// </summary>
        AButton,

        /// <summary>
        /// The b button
        /// </summary>
        BButton,

        /// <summary>
        /// The c button
        /// </summary>
        CButton,

        /// <summary>
        /// The d button
        /// </summary>
        DButton,

        /// <summary>
        /// The left shoulder button
        /// </summary>
        LeftShoulderButton,

        /// <summary>
        /// The right shoulder button
        /// </summary>
        RightShoulderButton,

        /// <summary>
        /// The left stick button
        /// </summary>
        LeftStickButton,

        /// <summary>
        /// The right stick button
        /// </summary>
        RightStickButton,

        /// <summary>
        /// The select button
        /// </summary>
        SelectButton,

        /// <summary>
        /// The start button
        /// </summary>
        StartButton,

        /// <summary>
        /// The d pad
        /// </summary>
        DPad,

        /// <summary>
        /// The left thumb stick
        /// </summary>
        LeftThumbStick,

        /// <summary>
        /// The right thumb stick
        /// </summary>
        RightThumbStick,

        /// <summary>
        /// The right trigger
        /// </summary>
        RightTrigger,

        /// <summary>
        /// The left trigger
        /// </summary>
        LeftTrigger
    }
}
